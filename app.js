function render() {
  const slug = currentSlug();
  const route = routeBySlug[slug];
  const pageTitle = route ? `${route.nav} | Video Roastery` : "문의가 들어오는 가게로 세팅합니다 | Video Roastery";
  const pageDesc = route ? route.lead : "홈페이지, AI 숏폼, 홍보영상, 문의 자동화까지 사장님의 온라인 영업 흐름을 고객이 이해하고 문의하게 만드는 구조로 정리합니다.";
  document.title = pageTitle;
  for (const [selector, value] of [
    ['meta[name="description"]', pageDesc],
    ['meta[property="og:title"]', pageTitle],
    ['meta[property="og:description"]', pageDesc]
  ]) {
    const el = document.querySelector(selector);
    if (el) el.setAttribute("content", value);
  }
  document.querySelector("#app").innerHTML = route ? detailPage(route) : mainPage();
  setupStickyCta();
}

function setupStickyCta() {
  const sticky = document.querySelector(".sticky");
  if (!sticky) return;
  const update = () => sticky.classList.toggle("is-visible", window.scrollY > 260);
  update();
  window.removeEventListener("scroll", window.__vrStickyUpdate);
  window.__vrStickyUpdate = update;
  window.addEventListener("scroll", update, { passive: true });
}

render();
